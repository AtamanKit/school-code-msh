point = (1, 2)
print(point)   # Returns '(1, 2)'


point = (1, 2) + (2, 4)
print(point)     # Returns '(1, 2, 3, 4)'


point = (1, 2) * 3
print(point)    # Returns '(1, 2, 1, 2, 1, 2)'


# List to Tuple
point = tuple([1, 2])
print(point)    # Returns '(1, 2)'


point = (1, 2, 3)
print(point[0:2])   # Returns '(1, 2)'
