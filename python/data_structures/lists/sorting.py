numbers = [7, 2, 5, 1]
print(numbers)

numbers.sort()
print(numbers)

# Invers sorting
numbers.sort(reverse=True)
print(numbers)

# Another way of sortin by creating a new list
new_numbers = [20, 90, 10, 70]
print(new_numbers)

new_new_numbers = sorted(new_numbers)
print(new_numbers)
print(new_new_numbers)
